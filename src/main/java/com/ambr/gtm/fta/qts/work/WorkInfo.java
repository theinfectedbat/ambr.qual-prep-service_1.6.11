package com.ambr.gtm.fta.qts.work;

public class WorkInfo
{
	private Long						bomKey;
	private Long						qtxKey;
	private Long						qtxWorkId;
	
	public Long getBomKey()
	{
		return bomKey;
	}

	public void setBomKey(Long bomKey)
	{
		this.bomKey = bomKey;
	}

	public Long getQtxKey()
	{
		return qtxKey;
	}

	public void setQtxKey(Long qtxKey)
	{
		this.qtxKey = qtxKey;
	}
	public Long getQtxWorkId()
	{
		return qtxWorkId;
	}

	public void setQtxWorkId(Long qtxWorkId)
	{
		this.qtxWorkId = qtxWorkId;
	}

}
