package com.ambr.gtm.fta.qts.util;

public class CompAnalysisConfData {
	private String compHsMatch;
	private String excludeTS;
	private String compType;
	private String ftaCode;
	private String excludeRvc;
	private String compHsLength;
	
	public String getCompHsMatch() {
		return compHsMatch;
	}
	public void setCompHsMatch(String compHsMatch) {
		this.compHsMatch = compHsMatch;
	}
	public String getExcludeTS() {
		return excludeTS;
	}
	public void setExcludeTS(String excludeTS) {
		this.excludeTS = excludeTS;
	}
	public String getCompType() {
		return compType;
	}
	public void setCompType(String compType) {
		this.compType = compType;
	}
	public String getFtaCode() {
		return ftaCode;
	}
	public void setFtaCode(String ftaCode) {
		this.ftaCode = ftaCode;
	}
	public String getExcludeRvc() {
		return excludeRvc;
	}
	public void setExcludeRvc(String excludeRvc) {
		this.excludeRvc = excludeRvc;
	}
	public String getCompHsLength() {
		return compHsLength;
	}
	public void setCompHsLength(String compHsLength) {
		this.compHsLength = compHsLength;
	}

}
